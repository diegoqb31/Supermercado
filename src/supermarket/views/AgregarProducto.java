    private void clear() {
        txtCANTIDAD.setText("");
        txtDESCRIPCION.setText("");
        txtEAN.setText("");
        txtPESO.setText("");
        txtPLU.setText("");
        txtPRECIO.setText("");
        txtTIPO.setText("");
    }

    private boolean checker() {
        boolean check = true;
        check = check && (!txtCANTIDAD.getText().isBlank() && isInteger(txtCANTIDAD.getText()));
        check = check && (!txtDESCRIPCION.getText().isBlank());
        check = check && (!txtEAN.getText().isBlank());
        check = check && (!txtPESO.getText().isBlank() && isFloat(txtPESO.getText()));
        check = check && (!txtPLU.getText().isBlank() && isInteger(txtPLU.getText()));
        check = check && (!txtPRECIO.getText().isBlank() && isFloat(txtPRECIO.getText()));
        check = check && (abarrotes.isSelected() || cuidadop.isSelected() || mercancias.isSelected() || frescos.isSelected());
        return check;
    }
    
    public String getTipo(){
        if(abarrotes.isSelected()){
            return "Abarrotes";
        }else{
            if(cuidadop.isSelected()){
                return "Cuidado Personal";
            }else{
                if(frescos.isSelected()){
                    return "Frecos";
                }else{
                    if(mercancias.isSelected()){
                        return "Mercancias";
                    }
                }
            }
        }
        return "";
    }

    public static boolean isInteger(String s) {
        try {
            Integer.parseInt(s);
        } catch (NumberFormatException e) {
            return false;
        } catch (NullPointerException e) {
            return false;
        }
        // only got here if we didn't return falsereturn true;
        return true;
    }

    public static boolean isFloat(String s) {
        try {
            Float.parseFloat(s);
        } catch (NumberFormatException e) {
            return false;
        } catch (NullPointerException e) {
            return false;
        }
        // only got here if we didn't return falsereturn true;
        return true;
    }

